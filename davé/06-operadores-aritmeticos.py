# Operadores Aritmeticos
# Soma
soma = 10 + 5
print(soma)
# subtracao
subtracao = 10 - 5
print(subtracao)
# Multiplicacao
multiplicacao = 10 * 5
print(multiplicacao)
# Divisao
divisao = 10 / 5
print(divisao)
# Divisao inteira
divisao_inteira = 10 // 5
print(divisao_inteira)
# Exponenciacao = 10 ** 2
print(exponenciacao)
# Modulo / Resto da divisao
modulo = 10 % 5
print(modulo)